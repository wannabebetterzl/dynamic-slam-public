def fill_paragraphs(doc: Document) -> None:
    texts = build_body_paragraphs()
    start = 11
    total = len(doc.paragraphs)
    if start + len(texts) > total:
        raise RuntimeError("模板段落数量不足，无法按原模板填充。")
    for idx, text in enumerate(texts, start=start):
        doc.paragraphs[idx].text = text
    for idx in range(start + len(texts), total):
        doc.paragraphs[idx].text = ""


